document.addEventListener('DOMContentLoaded', () => {
    const themeBtn = document.getElementById('themeToggle');
    const langSelect = document.getElementById('langSelect');

    // Theme Toggle
    if (themeBtn) {
        themeBtn.addEventListener('click', () => {
            const currentTheme = document.documentElement.getAttribute('data-theme');
            const newTheme = currentTheme === 'dark' ? 'light' : 'dark';
            document.documentElement.setAttribute('data-theme', newTheme);
            localStorage.setItem('theme', newTheme);
            updateThemeIcon(newTheme);
        });
    }

    // Sidebar Toggle for Mobile (Optional enhancement)
    const sidebar = document.querySelector('.sidebar');
    const mainLayout = document.querySelector('.main-layout');
    
    if (window.innerWidth < 768 && sidebar) {
        // Create a toggle button for mobile if it doesn't exist
        const toggleBtn = document.createElement('button');
        toggleBtn.innerHTML = '☰';
        toggleBtn.className = 'btn';
        toggleBtn.style.position = 'fixed';
        toggleBtn.style.bottom = '20px';
        toggleBtn.style.right = '20px';
        toggleBtn.style.zIndex = '1002';
        toggleBtn.style.borderRadius = '50%';
        toggleBtn.style.width = '50px';
        toggleBtn.style.height = '50px';
        toggleBtn.style.padding = '0';
        toggleBtn.style.fontSize = '24px';
        
        document.body.appendChild(toggleBtn);
        
        toggleBtn.addEventListener('click', () => {
            if (sidebar.style.display === 'flex') {
                sidebar.style.display = 'none';
            } else {
                sidebar.style.display = 'flex';
                sidebar.style.width = '100%';
            }
        });
    }

    // Load saved theme
    const savedTheme = localStorage.getItem('theme') || 'light';
    document.documentElement.setAttribute('data-theme', savedTheme);
    updateThemeIcon(savedTheme);

    function updateThemeIcon(theme) {
        if (themeBtn) {
            themeBtn.innerText = theme === 'dark' ? '☀️' : '🌙';
        }
    }

    // Language Change
    if (langSelect) {
        langSelect.addEventListener('change', (e) => {
            const lang = e.target.value;
            localStorage.setItem('lang', lang);
            applyTranslations(lang);
        });

        // Load saved lang, default to 'fr' if 'mg' or nothing was saved
        let savedLang = localStorage.getItem('lang');
        if (!savedLang || savedLang === 'mg') {
            savedLang = 'fr';
            localStorage.setItem('lang', 'fr');
        }
        langSelect.value = savedLang;
        applyTranslations(savedLang);
    }

    function applyTranslations(lang) {
        const trans = translations[lang];
        if (!trans) return;
        
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (trans[key]) {
                if (el.tagName === 'INPUT' && (el.type === 'text' || el.type === 'number')) {
                    el.placeholder = trans[key];
                } else {
                    el.innerText = trans[key];
                }
            }
        });
    }
});
